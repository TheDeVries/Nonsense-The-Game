import pygame
pygame.init()
class Maze:
    tile_size = 96
    map_height = 25
    map_width = 25
    x_camera = 0
    y_camera = 0
    move_camera = 0
    eno = 5
    def __init__(self):
        self.running = True
        self.wn = pygame.display.set_mode((800,600))
        self.player = pygame.image.load("Sprites//character_walk.png")
        self.mountain = pygame.image.load("Sprites//grass_mount1.png")
        self.grass = pygame.image.load("Sprites//grass.png")
        while self.running:
            for event in pygame.event.get():
                # Quit button
                if event.type == pygame.QUIT:
                    pygame.quit()
                    exit()
                # Keybinds
                if event.type == pygame.KEYDOWN:
                    if event.key == pygame.K_ESCAPE:
                        pygame.quit()
                        exit()
                if event.type == pygame.KEYDOWN:
                    if event.key == pygame.K_w:
                        Maze.move_camera = 1
                    elif event.key == pygame.K_a:
                        Maze.move_camera = 2
                    elif event.key == pygame.K_s:
                        Maze.move_camera = 3
                    elif event.key == pygame.K_d:
                        Maze.move_camera = 4
                elif event.type == pygame.KEYUP:
                    Maze.move_camera = 0
            if Maze.move_camera == 1:
                Maze.y_camera -= Maze.eno
            elif Maze.move_camera == 2:
                Maze.x_camera -= Maze.eno
            elif Maze.move_camera == 3:
                Maze.y_camera += Maze.eno
            elif Maze.move_camera == 4:
                Maze.x_camera += Maze.eno
            self.wn.fill((0,0,0))
            self.map_build()
            pygame.display.flip()
